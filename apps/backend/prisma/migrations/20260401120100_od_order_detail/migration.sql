  COD_ESTILO VARCHAR(100) NULL,
  DES_TELA VARCHAR(200) NULL,
  NOM_ESTILO VARCHAR(100) NULL,
  COLOR_AWAY VARCHAR(50) NULL,
  FONDO_TELA VARCHAR(100) NULL,
  VERSION_TELA VARCHAR(50) NULL,
  ORDER_SAMPLE TINYINT(1) NULL,
  SIZE_00 INT NULL,
  SIZE_1_2 INT NULL,
  SIZE_2 INT NULL,
  SIZE_2_3 INT NULL,
  SIZE_4 INT NULL,
  SIZE_4_5 INT NULL,
  SIZE_5 INT NULL,
  SIZE_6 INT NULL,
  SIZE_7 INT NULL,
  SIZE_8 INT NULL,
  SIZE_9 INT NULL,
  SIZE_10 INT NULL,
  SIZE_11 INT NULL,
  SIZE_12 INT NULL,
  SIZE_13 INT NULL,
  SIZE_14 INT NULL,
  SIZE_15 INT NULL,
  SIZE_16 INT NULL,
  SIZE_L INT NULL,
  SIZE_M INT NULL,
  SIZE_S INT NULL,
  SIZE_XL INT NULL,
  SIZE_XS INT NULL,
  SIZE_XXL INT NULL,
  SIZE_XXS INT NULL,
  TOTAL_ESTILO INT NULL,
